                    <h2>Parliament Hill 'Swab Event' Offers Hope to Canada's 777 Patients Waiting for Stem Cell Transplants</h2>
                    <p><em>Chair National Health Committee of Canada, MP Joy Smith, joins MP Terrence Young to draw greater awareness to Canada's need for more stem cell donors</em></p>
                    <p><strong>OTTAWA, October 6, 2010</strong> - With a goal of registering 200 new stem cell registrants Members of Parliament, Mr. Terence Young and Mrs. Joy Smith are joining forces with Canadian Blood Services' OneMatch Stem Cell and Marrow Network for a stem cell awareness and 'buccal-swab' event on Parliament Hill.</p>
                    <p>The slogan for the event is, OneSwab, OneMatch, OneLife. It will be held in the Commonwealth Room (238S) in Centre Block. The event will run from 3:15pm to 7:00pm, and is open to all Members of Parliament, Senators and Parliamentary staff between the ages of 17 and 50.</p>
                    <p>Member of Parliament for Oakville, Terence Young is holding this event in memory of David Smyth who, before passing away from his illness, helped raise awareness to the need for more stem cell donors in Canada. "The swab sample is absolutely painless and easy to do," says Mr. Young. "If they understand that, most people would be happy to take ten minutes and provide a cheek cell sample. Everyone in the targeted range group is welcome."</p>
                    <p>Joy Smith, Member of Parliament, for Kildonan-St. Paul is an advocate for stem cell awareness. Through this event, she hopes to encourage more Canadians to be tested as potential stem cell donors. "As Chair of the National Health Committee of Canada and as a sitting member of parliament, it is crucial all Canadians understand how important it is to make a lifesaving difference in someone's life by signing up as a OneMatch donor," says MP Joy Smith. "And as Parliamentarians, we take this campaign back to our communities with determination to increase Canada's stem cell Network."</p>
                    <p>OneMatch maintains a national network of volunteer stem cell donors - the eighth largest in the world, and is apart of an international network of more than 63 stem cell registries in 44 countries. Canada currently has over 260,000 registrants. According to Sue Smith, Executive Director for the OneMatch Network, everyone coming down to register today is helping. "One more person on the Network is one more possible match for a patient in need"</p>
                    <h3>About Canadian Blood Services</h3>
                    <p>Canadian Blood Services is a national, not-for-profit charitable organization that manages the supply of blood and blood products in all provinces and territories outside of Quebec. Canadian Blood Services also oversees the OneMatch Stem Cell and Marrow Network, and provides national leadership for organ and tissue donation and transplantation. Canadian Blood Services operates 42 permanent collection sites and more than 20,000 donor clinics annually. The provincial and territorial Ministries of Health provide operational funding to Canadian Blood Services. The federal government, through Health Canada, is responsible for regulating the blood system. For more information, please visit our website at <a href="www.blood.ca">www.blood.ca</a></p>
